import numpy as np
import scipy.stats as stats

# 1. Ввод данных из таблицы (по строкам)
control = [16.2, 15.8, 14.0, 15.1]       # Без удобрений
n20p20  = [17.5, 18.9, 16.7, 18.2]       # N20P20
n40p20  = [18.9, 18.7, 19.3, 20.6]       # N40P20
n60p20  = [26.3, 24.5, 24.3, 25.5]       # N60P20
n80p20  = [23.0, 23.9, 24.2, 23.9]       # N80P20

# Объединяем в один список для удобства работы
groups = [control, n20p20, n40p20, n60p20, n80p20]

# 2. Однофакторный дисперсионный анализ
f_statistic, p_value = stats.f_oneway(*groups)

print("=== Результаты дисперсионного анализа ===")
print(f"F-статистика: {f_statistic:.2f}")
print(f"p-значение: {p_value:.5e}")

# Проверка достоверности при заданном уровне значимости
alpha = 0.05
if p_value < alpha:
    print(f"\nВывод по достоверности: p-значение ({p_value:.5f}) меньше уровня значимости alpha = {alpha}.")
    print("Влияние минеральных удобрений на урожайность ДОСТОВЕРНО.")
else:
    print(f"\nВывод по достоверности: p-значение ({p_value:.5f}) больше уровня значимости alpha = {alpha}.")
    print("Влияние минеральных удобрений на урожайность НЕДОСТОВЕРНО.")

# 3. Расчет степени влияния фактора (Эта-квадрат, η²)
# Формула: η² = SSB / SSE (межгрупповая сумма квадратов / общая сумма квадратов)

# Объединяем все данные в один массив для нахождения общей средней
all_data = np.concatenate(groups)
total_mean = np.mean(all_data)

# Общая сумма квадратов (SSE)
sse = np.sum((all_data - total_mean)**2)

# Межгрупповая сумма квадратов (SSB)
ssb = 0

for group in groups:
    group_mean = np.mean(group)
    ssb += 4 * (group_mean - total_mean)**2

eta_squared = ssb / sse

print("\n=== Оценка степени влияния ===")
print(f"Показатель Эта-квадрат (η²): {eta_squared:.4f}")
print(f"Степень влияния минеральных удобрений составляет: {eta_squared * 100:.1f}%")